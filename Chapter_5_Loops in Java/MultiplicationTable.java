public class MultiplicationTable {
    /** Main method */
    public static void main(String[] args) {
      // Display the table heading
      System.out.println("           Multiplication Table");
  
      // Display the number title
      System.out.print("    ");
      for (int j = 1; j <= 9; j++)
        System.out.print("    " + j);
  
      System.out.println("\n-----------------------------------------");
  

    }
  }

  